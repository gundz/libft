#include	<stdlib.h>

void	ft_strdel(char **as)
{
	int		i;

	if (as == NULL)
		return ;
	i = 0;
	while (as[i] != '\0')
	{
		free(as[i]);
		i++;
	}
	free(as[i]);
	free(as);
}
